'''
This module used to create math calculation.
'''

def addition(a, b):
    '''
    It take two integer number only and return sum(+).
    '''
    return a + b

def substraction(a, b):
    '''
    It take two integer number only and return sub(-).
    '''
    return a - b

def multiplication(a, b):
    '''
    It take two integer number only and return mul(-).
    '''
    return a * b

def division(a, b):
    '''
    It take two integer number only and return div(/).
    '''
    return a / b

